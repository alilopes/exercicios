'''
Faça um programa que leia um número de ponto flutuante e mostre na tela o seu dobro e a sua terça parte.
'''

# Leio um número
numero = float(input('Número: '))

# Imprimo na tela o dobro deste número e a terça parte
print(f'O dobro de {numero} é {numero * 2:.2f}')
print(f'A terça parte de {numero} é {numero / 3:.2f}')
